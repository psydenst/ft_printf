#include "libftprintf.h"

int	ft_putstr(char *s)
{
	int i;
	i = 0;
	if(s == NULL)
    {
        write(1, "(null)", 6);
    }
    while(s[i] != '\0')
	{
		write(1, &s[i++], 1);
	}
    return(i);
}
